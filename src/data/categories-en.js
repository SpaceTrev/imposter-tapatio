export function getCategoryById_EN(id) {
  return CATEGORIES_EN.find((c) => c.id === id);
}

export function getRandomCategory_EN({ allowAdult }) {
  const allowedTypes = allowAdult ? ["family", "adult", "mixed"] : ["family", "mixed"];
  const filtered = CATEGORIES_EN.filter((c) => allowedTypes.includes(c.type));
  return filtered[Math.floor(Math.random() * filtered.length)];
}

export function randomPairFromCategory_EN(cat) {
  const idx = Math.floor(Math.random() * cat.pairs.length);
  return cat.pairs[idx];
}
